                            When a person gets his trademark registered, he acquires valuable rights to the use of the
                            trademark in connection with the goods in respect of which it is registered. And, in the
                            event of any invasion of his rights by any other person using a mark which is identical or
                            deceptively similar to his trademark, he could protect his mark by an action for
                            infringement and obtain an injunction. Thus, the basis for protecting the trademark is that
                            no mark shall be registered which is likely to deceive the public or which creates confusion
                            in the minds of the public in regard to the ‘origin’ or source of goods or services. When
                            two businesses compete with trademarks that sound similar to the consumers, trademark and
                            brand identity challenges frequently arise.
                            <br><br>
                            For determining deceptive similarity there is a requirement to examine ‘whether the
                            essential features of the plaintiff’s trade mark are to be found in that used by the
                            defendant’. There is no hard and fast rule to laid down a universal test to determine the
                            similarity between two marks. When the class of buyers is quite educated and rich, the test
                            to be applied is different from the one where the product would be purchased by villagers,
                            illiterate and poor. Such is the case of Pernod Ricard India Private Limited & Anr. v.
                            Karanveer Singh Chhabra, where two premium whiskey brands ‘Blenders Pride’ and ‘London
                            Pride’ engaged in a trademark battle which highlights the complexities of trademark law. In
                            this article, we critically analyse the dispute and the findings of the courts seeking
                            clarity on the interpretation of deceptive similarity.
                        </h5>
