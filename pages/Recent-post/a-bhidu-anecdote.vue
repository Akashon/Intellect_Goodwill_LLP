                        class="inline-flex w-full rounded-b-2xl border-x-2 border-b-2 border-dashed border-gray-600/30 bg-blue-100/50 px-4 py-4 text-gray-600">
                        <h5>
                            In the case of Jaikishan Kakubhai Saraf Alias Jackie Shroff Vs The Peppy Store & Ors. (
                            Jackie Shroff v. The Peppy Store), Jackie Shroff, the plaintiff in this case, is a highly
                            acclaimed Indian actor with a prolific career spanning over several decades. Known for his
                            roles in more than 220 films, television shows, and web series, Shroff has built a
                            substantial reputation and public persona. Beyond his acting career, he has endorsed a wide
                            array of products and services, further solidifying his public image. His distinctive
                            attributes, including his name, image, voice, and unique style, are recognized and
                            associated with him globally. In addition to personality rights, the plaintiff also asserted
                            his rights under trademark law.
                            <br><br>

                            Jackie Shroff is the registered proprietor of the trademark “BHIDU” under registration
                            numbers 3227968 in Class 25, covering ready-made garments, hosiery, and articles of
                            clothing, and 3227969 in Class 41, covering entertainment, education, providing of training,
                            film production, sporting and cultural activities. Furthermore, he holds the trademark
                            “Bhidu Ka Khopcha” under registration number 4362494 in Class 41. These trademarks are
                            crucial as they signify Shroff’s personal brand and are associated exclusively with him. The
                            unauthorized use of these trademarks by any entity is likely to cause confusion among
                            consumers and dilute the distinctiveness of Shroff’s brand.
                            <br><br>

                            The defendants in this case were involved in a range of unauthorized activities that
                            allegedly infringed on Jackie Shroff’s personality rights. These activities included the
                            creation and sale of merchandise bearing Shroff’s image and name without his consent.
                            Specifically, defendants were accused of selling various products, such as animated wall
                            art, videos, and even AI chatbots that mimicked Shroff’s voice and likeness. Defendant No. 1
                            (The Peppy Store) and No. 2 (Frankly Retail Private Limited) were highlighted for their
                            direct involvement in selling such products. These activities were conducted without seeking
                            permission or providing any form of compensation to Shroff, leading to claims of
                            unauthorized commercial exploitation of his personality.
                            <br><br>

                            In response to the allegations, Defendant No. 1’s counsel argued that the artwork in
                            question, despite resembling Shroff, was independently created and thus should be considered
                            fair use. They also pointed out a previous instance where Shroff appeared to have endorsed
                            their work, suggesting implied consent or at least tolerance of their use of his likeness.
                            On the other hand, Defendant No. 2 took a different approach by removing the disputed
                            merchandise from their website. However, they continued to contest the case, possibly
                            challenging the scope of Shroff’s personality rights or the applicability of the law in this
                            context. These defences indicate a broader debate about the boundaries of personality rights
                            and the concept of fair use in creative works.
                            <br><br>

                            The court found that there was a prima facie case of infringement of Jackie Shroff’s
                            personality rights. This conclusion was based on the clear, unauthorized use of Shroff’s
                            name, image, voice, and other distinctive attributes in various commercial products and
                            media without his consent. The court recognized that Jackie Shroff has a significant
                            reputation and goodwill attached to his persona, and any unauthorized use of his likeness
                            could mislead the public and dilute his brand. The court emphasized the importance of
                            protecting such rights to prevent the unfair exploitation of a celebrity’s established fame.
                            The balance of convenience was found to favor the plaintiff, Jackie Shroff, because the
                            continued unauthorized use of his personality would cause him irreparable harm, including
                            potential damage to his reputation and loss of commercial opportunities. The court
                            highlighted that monetary compensation alone would not suffice to remedy such harm,
                            necessitating the need for an immediate injunction.
                            <br><br>

                            As a result of these findings, the court issued an ex-parte ad-interim injunction against
                            several defendants. An ex-parte injunction means that the order was given without requiring
                            the presence of the defendants at the hearing, highlighting the urgency and the clear nature
                            of the infringement. The injunctions specifically restrained the defendants from using
                            Jackie Shroff’s name, image, voice, and other distinctive attributes for any commercial
                            purposes without his explicit consent. This included a wide range of uses such as on
                            merchandise, in videos, and through AI chatbots designed to mimic Shroff’s persona. The
                            court’s orders were explicit and comprehensive, covering all possible avenues of
                            unauthorized commercial exploitation. This legal action aimed to immediately halt any
                            ongoing infringement activities and prevent further unauthorized use of Shroff’s
                            personality, thus protecting his rights and reputation from further harm.
                            <br><br>

                            In addition to granting injunctions against the defendants, the court issued directions to
                            relevant government authorities to ensure compliance with its orders. The Department of
                            Telecommunications (DoT) and the Ministry of Electronics and Information Technology (MeitY)
                            were specifically instructed to take necessary actions to enforce the court’s injunctions.
                            These directions likely included monitoring and regulating online and electronic platforms
                            to ensure that the infringing materials were removed and that no further unauthorized use
                            occurred. This involvement of governmental bodies underscored the seriousness of the
                            infringement and the need for a coordinated effort to protect Shroff’s personality rights.
                            The court’s directive to these authorities aimed to leverage their regulatory powers to
                            effectively curb the misuse of Shroff’s persona across various media and digital platforms,
                            ensuring comprehensive enforcement of the court’s order.

