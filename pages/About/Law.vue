                                income, including income outside India. Non-residents are taxable only for the income.
                                received in India or Income accrued in India. Not ordinarily residents are taxable in
                                relation to income received in India or income accrued in India and income from business
                                or profession controlled from India. <br>
                                ii. Registration Procedures.
