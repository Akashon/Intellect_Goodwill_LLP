                                <P class="font-bold ">Property Tax:</P>
                                Property tax or ‘house tax’ is a local tax on buildings, along with appurtenant land,
                                and imposed on owners. The tax power is vested in the states and it is delegated by law
                                to the local bodies, specifying the valuation method, rate band, and collection
                                procedures. The tax base is the annual rateable value (ARV) or area-based rating.
                                Owner-occupied and other properties not producing rent are assessed on cost and then
